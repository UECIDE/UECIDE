import com.ardublock.translator.block.TranslatorBlock;
import com.ardublock.translator.block.exception.SocketNullException;
import com.ardublock.translator.block.exception.SubroutineNotDeclaredException;

public class Keyboard_ReleaseAll extends TranslatorBlock
{
	public Keyboard_ReleaseAll(Long blockId, Translator translator, String codePrefix, String codeSuffix, String label)
	{
		super(blockId, translator, codePrefix, codeSuffix, label);
	}

	@Override
	public String toCode() throws SocketNullException, SubroutineNotDeclaredException
	{
		
		translator.addSetupCommand("Keyboard.begin();");
		
		String ret="Keyboard.releaseAll();\n";
    	
		return ret;

	}
}
